def total_solution(data):
    pairs = {
        '{':'}',
        '(':')',
        '[':']',
        '<':'>'
    }
    error_weigths = {
        ')': 3,
        ']': 57,
        '}': 1197,
        '>': 25137,
    }
    incomplete_weigths = {
        ')': 1,
        ']': 2,
        '}': 3,
        '>': 4,
    }
    errors_sum = 0
    incomplete_scores = []
    for line in data:
        matches = []
        for char in line:
            close_match_value = pairs.get(char, False)
            if close_match_value:
                matches.append(close_match_value)
                continue
            poped = matches.pop()
            if char != poped:
                errors_sum += error_weigths.get(char, 0)
                break
        else:
            incomplete_score = 0
            while matches:
                incomplete_score = 5 * incomplete_score + incomplete_weigths[matches.pop()]
            incomplete_scores.append(incomplete_score)
    incomplete_scores.sort()

    return errors_sum, incomplete_scores[len(incomplete_scores)//2]

